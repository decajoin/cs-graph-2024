// 通过点阵法显示字符
void drawPattern(int pattern[16][16], int startX, int startY)
{
    for (int i = 0; i < 16; i++)
    {
        for (int j = 0; j < 16; j++)
        {
            if (pattern[i][j] == 1)
            {
                putpixel(startX + j * 10, startY + i * 10, WHITE);
            }
        }
    }
}

